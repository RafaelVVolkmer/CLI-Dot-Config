return {
  {
    "nvim-treesitter/nvim-treesitter",
    event = { "BufReadPost", "BufNewFile" },
    build = ":TSUpdate",
    config = function()
      local ok, configs = pcall(require, "nvim-treesitter.configs")
      if not ok then
        return
      end

      configs.setup({
        ensure_installed = { "c", "cpp", "lua", "bash", "vim", "vimdoc", "json" },
        highlight = { enable = true },
        indent = { enable = true },
      })
    end,
  },
}
